package com.Nishant.Basic.String;

public class PositionOfChar {
    public static void main(String[] args) {
        //Here We Find Position Of Characters In a String
        String name1 = "Nishant Saraswat";
        // Use charAt to find the position of a Character
        /* Note that First Character is At 0th Position In It
        And then 2nd , 3rd, ..........
         */
        System.out.println(name1.charAt(0));//Here 0th character is N// Space will counted like a char
        System.out.println(name1.charAt(8));

    }

}
