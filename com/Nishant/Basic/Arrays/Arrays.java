package com.Nishant.Basic.Arrays;

public class Arrays {
    public static void main(String[] args) {
        // To Make List Of Many Value
        // Because It Saves us from Creating Many Variable
        // By It We Have Not to create Large No. Of Variable
        // We can Create Arrays Of Integers, Strings, And Also Of Boolean
        // Note:- By Arrays We Can Store Any Type Of Data And Also Access That Data
        int [] marks/*[]-to show list or array*/ = new int[3];// Here "3" Denotes The no. of Box we need
        marks[0]/*[0]shows position*/ = 97;
        marks[1] = 95;
        marks[2] = 98;
        System.out.println(marks[1]);
        System.out.println(marks[0]);


    }
}
